function Z = PotCorr(Zm,Cal,f)
%**************************************************************************
%
%**************************************************************************
w = 2*pi*f;

Cm = Cal(1);
Cs = Cal(2);
t1 = Cal(3);
t2 = Cal(4);
A2 = Cal(5);
B2 = Cal(6);
A3 = Cal(7);
C4 = Cal(8);
Zr = Cal(9)./(1+1i*w*Cal(9)*Cal(10));
Rm = Cal(11);

Fil = (A3*(1i*w*t1).^3 + A2*(1i*w*t1).^2 + (1i*w*t1) + 1)./(B2*(1i*w*t2).^2 + (1i*w*t2) + 1);

Z = (Zm.*(1+1i*w*(C4+Cs/2).*Zr) + 1i*w*C4.*Zr*Rm)./(Fil.*(1+1i*w*Cm*Rm) + (w.^2)*C4*Cs/2*Rm.*Zr - 1i*w*Cs/2.*Zm.*(Fil+1+1i*w*(C4+Cs/2).*Zr));

return